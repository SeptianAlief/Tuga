
package TugasPraktikum10;

public class Matakuliah {
    String kd_matkul;
    String nama_matkul;
    String kode_dosden;
    String nama_dosen;

    public String getKd_matkul() {
        return kd_matkul;
    }

    public void setKd_matkul(String kd_matkul) {
        this.kd_matkul = kd_matkul;
    }

    public String getNama_matkul() {
        return nama_matkul;
    }

    public void setNama_matkul(String nama_matkul) {
        this.nama_matkul = nama_matkul;
    }

    public String getKode_dosden() {
        return kode_dosden;
    }

    public void setKode_dosden(String kode_dosden) {
        this.kode_dosden = kode_dosden;
    }

    public String getNama_dosen() {
        return nama_dosen;
    }

    public void setNama_dosen(String nama_dosen) {
        this.nama_dosen = nama_dosen;
    }
    
    
}
